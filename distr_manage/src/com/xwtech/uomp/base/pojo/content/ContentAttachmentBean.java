package com.xwtech.uomp.base.pojo.content;

public class ContentAttachmentBean {
private String  attachId;
private String  docId;
private String  attachName;
private String  type;
private String  attachDir;
private String  attachUrl;
private String  interfaceDir;
public String getAttachId() {
	return attachId;
}
public void setAttachId(String attachId) {
	this.attachId = attachId;
}
public String getDocId() {
	return docId;
}
public void setDocId(String docId) {
	this.docId = docId;
}
public String getAttachName() {
	return attachName;
}
public void setAttachName(String attachName) {
	this.attachName = attachName;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public String getAttachDir() {
	return attachDir;
}
public void setAttachDir(String attachDir) {
	this.attachDir = attachDir;
}
public String getAttachUrl() {
	return attachUrl;
}
public void setAttachUrl(String attachUrl) {
	this.attachUrl = attachUrl;
}
public String getInterfaceDir() {
	return interfaceDir;
}
public void setInterfaceDir(String interfaceDir) {
	this.interfaceDir = interfaceDir;
}

}
