/**
 * @author zhanglu
 *
 */
public class ConsoleBean {

	private String monthStr;
	private long countNum;
	private String city;
	private String areaName;
	private String bossCode;
	private String sumMoney;
	
	/**
	 * @return the sumMoney
	 */
	public String getSumMoney() {
		return sumMoney;
	}
	/**
	 * @param sumMoney the sumMoney to set
	 */
	public void setSumMoney(String sumMoney) {
		this.sumMoney = sumMoney;
	}
	/**
	 * @return the areaName
	 */
	public String getAreaName() {
		return areaName;
	}
	/**
	 * @param areaName the areaName to set
	 */
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	/**
	 * @return the bossCode
	 */
	public String getBossCode() {
		return bossCode;
	}
	/**
	 * @param bossCode the bossCode to set
	 */
	public void setBossCode(String bossCode) {
		this.bossCode = bossCode;
	}
	/**
	 * @return the monthStr
	 */
	public String getMonthStr() {
		return monthStr;
	}
	/**
	 * @param monthStr the monthStr to set
	 */
	public void setMonthStr(String monthStr) {
		this.monthStr = monthStr;
	}
	/**
	 * @return the countNum
	 */
	public long getCountNum() {
		return countNum;
	}
	/**
	 * @param countNum the countNum to set
	 */
	public void setCountNum(long countNum) {
		this.countNum = countNum;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	
	
}
